package org.bloomreach.forge.marketplace.generator;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

/**
 * Represents an artifact entry in the addon config file.
 * Allows developers to configure multi-artifact add-ons with explicit targets and scopes.
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class ConfigArtifact {

    private String groupId;
    private String artifactId;
    private String target;
    private String scope;
    private String type;
    private String description;

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public String getArtifactId() {
        return artifactId;
    }

    public void setArtifactId(String artifactId) {
        this.artifactId = artifactId;
    }

    public String getTarget() {
        return target;
    }

    public void setTarget(String target) {
        this.target = target;
    }

    public String getScope() {
        return scope;
    }

    public void setScope(String scope) {
        this.scope = scope;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
